# List Indexing

mylist = ['a', 'b', 'c', 'd', 'e']

z = mylist[2]
print(z)  # This will print: c
print(type(z))  # This will print: <class 'str'>
print(type(mylist))  # This will print: <class 'list'>

print(mylist.index('a'))  # This will print: 0
print(dir(mylist))  # This will print all the methods available for list objects